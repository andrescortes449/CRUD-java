public class POJO {
    
    private String id;
    private String nombre;
    private String email;
    private String sexo;

    public POJO(String id, String nombre, String email, String sexo) {
        this.id = id;
        this.nombre = nombre;
        this.email = email;
        this.sexo = sexo;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    

}
